class VariableTest1 {
    public static void main(String[] args) {
        byte b1 = 12;
        byte b2 = -128;
        System.out.println(b1);
        System.out.println(b2);

        char c1 = 'ол';
        char c2 = 'д─';
        char c3 = 'це';
        char c4 = '4';
        char c5 = '\n';
        char c6 = '\u0043';
        System.out.println(c1);
        System.out.println(c2);
        System.out.print(c5);
        System.out.println(c3);
        System.out.println(c4);
        System.out.println(c6);
    }
}